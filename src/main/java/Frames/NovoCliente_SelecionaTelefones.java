    private void btNovoCelular1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNovoCelular1ActionPerformed
        int row = tbTelefones.getSelectedRow();
        if(row > -1)
        {
            modeloTelefones.removeRow(row);
            commit();
        }
    }//GEN-LAST:event_btNovoCelular1ActionPerformed

    private void commit()
    {
        this.telefones = new ArrayList<>();
        TelefoneCliente t;
        for(int i=0; i<tbTelefones.getRowCount();i++)
        {
            t = new TelefoneCliente();
            String s = (String)tbTelefones.getValueAt(i, 1);
            s = s.replace(" ", "").replace("-", "").replace("(", "").replace(")", "");
            t.setTelefone(s);
            t.setTipo((String)tbTelefones.getValueAt(i, 0));
            this.telefones.add(t);
        }
    }
    

