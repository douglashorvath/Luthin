    public void preenxeTabelaTelefones()
    {
        modeloTelefones.setNumRows(0);
        String[] linha = new String[2];
        if(telefones != null)
        {
            for(int i=0; i<telefones.size(); i++)
            {
                linha[0] = telefones.get(i).getTipo();
                if("FIXO".equals(linha[0]))
                {
                    linha[1] = Formatar.format("(##) ####-####", telefones.get(i).getTelefone());
                }
                if("MOVEL".equals(linha[0]))
                {
                    linha[1] = Formatar.format("(##) #####-####", telefones.get(i).getTelefone());
                }
                modeloTelefones.addRow(linha);
            }
        }
    }

    public ArrayList<TelefoneCliente> getTelefones() {
        return telefones;
    }

    public void setTelefones(ArrayList<TelefoneCliente> telefones) {
        this.telefones = telefones;
        if(this.telefones == null)
        {
            this.telefones = new ArrayList<>();
        }
        preenxeTabelaTelefones();
    }

    
    
